int	ft_any(char **tab, int (*f) (char*))
{
	int	i;

	i = 0;
	while (*tab != 0)
	{
		if (f(*tab) == 1)
			i++;
		tab++;
	}
	if (i != 0)
		return (1);
	return (0);
}
